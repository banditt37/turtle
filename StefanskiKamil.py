import turtle

s = turtle.getscreen()
t = turtle.Turtle()
t.speed(8)

def base():
    t.penup()
    t.goto(-250, 0)
    t.pendown()
    t.color("black", "brown")
    t.begin_fill()
    for i in range(3):
        t.fd(500)
        t.rt(90)
    t.fd(500)
    t.end_fill()
    t.penup()

def square(x, y, outline, fill, rt, fd):
    t.penup()
    t.goto(x, y)
    t.pendown()
    t.color(outline, fill)
    t.begin_fill()
    for i in range(4):
        t.rt(rt)
        t.fd(fd)
    t.end_fill()
    t.penup()

def right(x, y, outline, fill, fd, rt, fdl):
    t.penup()
    t.goto(x, y)
    t.pendown()
    t.color(outline, fill)
    t.begin_fill()
    t.fd(fd)
    t.rt(rt)
    t.fd(fdl)
    t.rt(rt)
    t.fd(fd)
    t.end_fill()
    t.penup()

def cross(x, y, outline, fill, fds, fdl, rt, lt):
    t.penup()
    t.goto(x, y)
    t.pendown()
    t.color(outline, fill)
    t.begin_fill()
    for i in range(4):
        t.fd(fds)
        t.rt(rt)
        t.fd(fdl)
        t.lt(lt)
        t.fd(fdl)
        t.rt(rt)
        t.fd(fds)
    t.end_fill()
    t.penup()

def semicircle(x, y, outline, fill, rt, c1, c2, lt,fd):
    t.penup()
    t.goto(x, y)
    t.pendown()
    t.color(outline, fill)
    t.begin_fill()
    t.rt(rt)
    t.circle(c1, c2)
    t.lt(lt)
    t.fd(fd)
    t.end_fill()
    t.penup()

def signature(x, y, outline, fill, text):
    t.penup()
    t.goto(x, y)
    t.pendown()
    t.color(outline, fill)
    t.write(text)
    t.penup()

def arrow(x, y, outline, fill, ltt, lt, rt, fd1, fd2, fd3):
    t.lt(ltt)
    t.penup()
    t.goto(x, y)
    t.pendown()
    t.color(outline, fill)
    t.begin_fill()
    t.fd(fd1)
    t.lt(lt)
    t.fd(fd2)
    t.rt(rt)
    t.fd(fd3)
    t.rt(lt)
    t.fd(fd3)
    t.rt(rt)
    t.fd(fd2)
    t.lt(lt)
    t.fd(fd1)
    t.rt(lt)
    t.end_fill()
    t.penup()

